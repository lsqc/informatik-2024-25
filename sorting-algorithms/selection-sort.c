void selection_sort(int t[], int n)
{
    int i = 0;
    for (; i++< n - 1;)
    {
        int m = i - 1, j = i - 1;
        for (;++j<n;)
        {
            {
                // *(t + j) = t[j] due to 4 byte offset 
                // j is the number of elements, NOT bytes!!
                if (*(t + j) < *(t + m))
                {
                    m = j;
                }
            } 
        }

        if (!(m == i - 1))
        {
            int u = t[m];
            t[m] = t[i - 1];
            t[i - 1] = u;
        }
    }
}

int main()
{
    int a[] = {79,8,29,87,4501,61,5981,601};
    int n = sizeof(a) / sizeof(a[0]);

    selection_sort(a, n);

    for (int i = 0; i++ < n;)
    {
        printf("%d. %d\n", i, a[i - 1]);
    }

    return 0;
}
